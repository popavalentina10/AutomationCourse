package ro.sit.homework;

public class Student {
    private String name;
    private Double average;

    public void setName(String name) {
        this.name = name;
    }

    public void setAverage(Double average) {
        this.average = average;
    }

    public String getName() {
        return name;
    }

    public Double getAverage() {
        return average;
    }
}
